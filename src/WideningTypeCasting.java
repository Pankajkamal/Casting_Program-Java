/*Widening Type Casting is use for convert smaller type to larger type size automatically*/
public class WideningTypeCasting {
	public static void main(String[]args) {
		int myInt = 10;
		double myDouble = myInt; //Automatically Casting: int to double.
		System.out.println(myInt);
		System.out.println("Automatically Casting: int to double.");
		System.out.println(myDouble);
		
	}
}
